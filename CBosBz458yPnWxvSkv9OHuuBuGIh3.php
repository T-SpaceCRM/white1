3. Знайте местные обычаи - В разных странах культурные нормы могут отличаться от тех, к которым мы привыкли дома; ознакомление с ними до приезда может сэкономить неловкая ситуация позже по дороге (или в самолете). Кроме того, изучение базовых фраз на их родном языке имеет большое значение для установления взаимопонимания с местными жителями, которые ценят инициативу иностранцев, даже если они не обязательно понимают все сказанное! 
4. Будьте в безопасности - Всякий раз, когда отправляетесь исследовать незнакомые места, всегда носите с собой карту, чтобы, если что-то случится вдали от дома, знать, как лучше всего безопасно вернуться, не заблудившись; дополнительно иметь копии всех важных документов, таких как паспорта / визы, если что-то произойдет при замене тоже нужно быстро получить!  

