 6. Будьте в курсе местных законов и обычаев - Ознакомьтесь с местными законами и обычаями перед въездом в иностранную страну; незнание не является оправданием при поездке за границу!  

 7. Проверьте, какое медицинское страхование применяется - Узнайте, какое медицинское страхование применяется как внутри страны, так и за рубежом, до отъезда из родной страны; многие страны требуют подтверждения наличия медицинской страховки при въезде на их границы, что может привести к депортация без предоставления надлежащих документов .    

