5. Веселитесь и развлекайтесь - и последнее, но, конечно, не менее важное: не забудьте выделить время для осмотра достопримечательностей (или даже деловых поездок), чтобы насладиться изучением различных культур и кухонь, доступных в каждом посещаемом месте, что придает неповторимый колорит любому проведенному отпуску!.</p>
