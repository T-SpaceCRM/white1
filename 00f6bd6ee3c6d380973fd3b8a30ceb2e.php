и вы разбогатеете, это произойдет не под эгидой Банка).4) Делайте разумные инвестиции.Строго придерживайтесь правила: храните только наличные, банковские карты и инвестиции (это самый надежный способ вложения денег, потому что он очень волатилен). 5) заботьтесь о своем здоровье.Сдайте анализы (как хороший вариант, ежегодное бесплатное медицинское обследование). 6) упражняйтесь с умом.Регулярно повышайте уровень своей физической подготовки, заботясь о своем здоровье (физические упражнения, диета, сон и общение). 7) сделать Умные деньги.Заведите хорошую привычку инвестировать 10-20% от каждого заработанного вами дохода (регулярно покупая валюту, акции и другие финансовые инструменты).Это позволит вам постепенно сформировать внушительный капитал, доход от которого полностью обеспечит вас и всю вашу семью. 1</p>
